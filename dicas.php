      <p>Obs.: Sendo utilizado recursos de institui&ccedil;&otilde;es financeiras, o comprador  disponibilizar&aacute; o valor da escritura na pr&oacute;pria institui&ccedil;&atilde;o e o registro ser&aacute;  pago no cart&oacute;rio.</p>
      <p>As certid&otilde;es negativas n&atilde;o t&ecirc;m custos para o  comprador. </p>
