    <td><p>No momento da assinatura da autoriza&ccedil;&atilde;o de venda  de um im&oacute;vel, o propriet&aacute;rio apresentar&aacute; c&oacute;pias dos seguintes documentos:</p>
      <ul>
        <li>RG e CPF dos  propriet&aacute;rios</li>
        <li>Matricula do  im&oacute;vel</li>
        <li>Carn&ecirc; do  IPTU</li>
        <li>Carn&ecirc; da  Engepasa</li>
        <li>Comprovante  do estado civil</li>
        <li>Carta de prefer&ecirc;ncia  no caso de im&oacute;vel alugado</li>
      </ul>
      <p>Tendo proposta assinada, o vendedor dever&aacute;  complementar com os seguintes documentos:</p>
      <ul>
        <li>3 &uacute;ltimas  contas de &aacute;gua, luz e g&aacute;s</li>
        <li>3 &uacute;ltimas  contas condominiais</li>
      </ul>
    <p>As  corretoras passar&atilde;o a apurar as condi&ccedil;&otilde;es e pend&ecirc;ncias de contas do im&oacute;vel, a  fim de ajudar, orientar e acompanhar a regulariza&ccedil;&atilde;o se houver necessidades.</p>
